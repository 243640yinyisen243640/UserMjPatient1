public class ImmunoFluorescenceReportListBean {
    /**
     * name : MYO
     * val :
     * ref : 1-10
     * unit : mg/L
     */

    private String name;
    private String val;
    private String ref;
    private String unit;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getVal() {
        return val;
    }

    public void setVal(String val) {
        this.val = val;
    }

    public String getRef() {
        return ref;
    }

    public void setRef(String ref) {
        this.ref = ref;
    }

    public String getUnit() {
        return unit;
    }

    public void setUnit(String unit) {
        this.unit = unit;
    }
}
